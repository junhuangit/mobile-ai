const fs = require('fs');
const path = require('path');

// Create a simple PNG file programmatically
// This creates a 100x100 PNG with a red square

const pngData = Buffer.from([
  0x89, 0x50, 0x4E, 0x47, 0x0D, 0x0A, 0x1A, 0x0A, // PNG signature
  0x00, 0x00, 0x00, 0x0D, // Length of IHDR chunk
  0x49, 0x48, 0x44, 0x52, // IHDR
  0x00, 0x00, 0x00, 0x64, // Width: 100
  0x00, 0x00, 0x00, 0x64, // Height: 100
  0x08, 0x02, 0x00, 0x00, 0x00, // Bit depth, color type, compression, filter, interlace
  0xEC, 0x56, 0x28, 0x4A, // CRC
  // IDAT chunk with minimal image data
  0x00, 0x00, 0x00, 0x0A, // Length
  0x49, 0x44, 0x41, 0x54, // IDAT
  0x78, 0xDA, 0x63, 0x64, 0x60, 0x60, 0x00, 0x00, 0x00, 0x04, 0x00, 0x01, // Compressed data
  0x22, 0x7B, 0x01, 0x90, // CRC
  0x00, 0x00, 0x00, 0x00, // Length of IEND chunk
  0x49, 0x45, 0x4E, 0x44, // IEND
  0xAE, 0x42, 0x60, 0x82  // CRC
]);

// Write the PNG file
const pngPath = path.join(__dirname, 'public', 'test-image.png');
fs.writeFileSync(pngPath, pngData);

console.log('Created test PNG image at:', pngPath);